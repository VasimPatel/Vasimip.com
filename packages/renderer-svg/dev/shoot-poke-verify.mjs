import puppeteer from 'puppeteer-core'
const SP = '/private/tmp/claude-501/-Users-vasimpatel-Projects-Vasimip-com/32aa8dcf-c680-4ebe-b81e-26481170ef2d/scratchpad/parity'
const b = await puppeteer.launch({ executablePath: '/Applications/Google Chrome.app/Contents/MacOS/Google Chrome', headless: 'new', args: ['--no-sandbox'], defaultViewport: { width: 1440, height: 900 } })
const p = await b.newPage()
const errs = []
p.on('pageerror', e => errs.push(e.message))
await p.goto('http://localhost:5173/', { waitUntil: 'networkidle2' })
await new Promise(r => setTimeout(r, 800))
await p.evaluate(() => window.__notebookGoTo(1))
await new Promise(r => setTimeout(r, 6000))
const box = async () => p.evaluate(() => {
  const el = document.querySelector('[data-dash-poke]')
  const r = el.getBoundingClientRect()
  return { x: r.x + r.width / 2, y: r.y + r.height / 2 }
})
// far cursor first, then near — dilation + lean check (crop face closely)
await p.mouse.move(150, 150)
await new Promise(r => setTimeout(r, 1000))
let c = await box()
await p.screenshot({ path: `${SP}/v-far.png`, clip: { x: c.x - 80, y: c.y - 110, width: 200, height: 200 } })
await p.mouse.move(c.x + 55, c.y - 30)
await new Promise(r => setTimeout(r, 1000))
c = await box()
await p.screenshot({ path: `${SP}/v-near.png`, clip: { x: c.x - 80, y: c.y - 110, width: 200, height: 200 } })
// poke: click and capture 3 frames wide (bubble visible)
await p.mouse.move(c.x, c.y)
await p.mouse.down(); await p.mouse.up()
await new Promise(r => setTimeout(r, 200))
await p.screenshot({ path: `${SP}/v-poke-1.png`, clip: { x: c.x - 200, y: c.y - 200, width: 460, height: 340 } })
await new Promise(r => setTimeout(r, 350))
await p.screenshot({ path: `${SP}/v-poke-2.png`, clip: { x: c.x - 200, y: c.y - 200, width: 460, height: 340 } })
await new Promise(r => setTimeout(r, 500))
await p.screenshot({ path: `${SP}/v-poke-3.png`, clip: { x: c.x - 200, y: c.y - 200, width: 460, height: 340 } })
console.log('errors:', errs.length ? errs.slice(0,3) : 'none')
await b.close()
